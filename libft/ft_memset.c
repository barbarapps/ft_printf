#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int	a;
	char			*r;

	a = 0;
	r = (char *)b;
	while (a < len)
	{
		r[a] = c;
		a++;
	}
	return (b);
}
